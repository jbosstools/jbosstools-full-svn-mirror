package org.hibernate.netbeans.console.model;

/**
 * @author leon
 */
public class StoreCity {

    private Long id;
    
    private String name;
    
    public StoreCity() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
